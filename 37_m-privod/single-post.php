                    <h2>Асинхронные электродвигатели</h2>
                    <p>Асинхронные электродвигатели переменного тока получили наибольшее распространение в промышленном производстве. Особенностью данных приводов является более высокая частота вращения магнитного поля по сравнению со скоростью вращения ротора.</p>
