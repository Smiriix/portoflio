                    <p>Тем не менее, при существенных недостатках этот тип двигателей отличается высоким пусковым моментом и стабильной работой в условиях больших перегрузок. Приводы данного типа чаще всего применяются в металлургии и станкостроении, устанавливаются на электротранспорт.</p>
