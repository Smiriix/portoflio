                    <div class="category--desc__text">
                        <p>По типу передачи</p>
                        <p>Мотор-редукторы классифицируют по ряду признаков, основным из которых является вид редукторной передачи. В зависимости от используемого редуктора различают следующие типы приводов:</p>
                        <ul>
                            <li>червячные</li>
                            <li>цилиндрические</li>
                            <li>планетарные</li>
                            <li>волновые</li>
                        </ul>
                        <p>Одно из основных преимуществ червячных мотор-редукторов — возможность произвольной компоновки. Конструктивные особенности агрегатов позволяют ориентировать выходной вал в любой плоскости. При этом взаимное расположение входного и выходного валов зависит от количества ступеней. Червячный мотор-редуктор компактен, универсален, обладает возможностью самоторможения. Многие приводы этого типа имеют унифицированные присоединительные размеры, позволяющие без проблем заменять вышедшее из строя оборудование.</p>
                    </div>
