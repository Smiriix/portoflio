                <div class="category--desc__wrapper">
                    <div class="category--desc__text">
                        <p>В цилиндрических мотор-редукторах выходные валы, как правило, располагаются только в горизонтальной плоскости. Данный недостаток компенсируется высоким КПД. По этому показателю цилиндрические мотор-редукторы превосходят все остальные виды приводного оборудования. Кроме того, такой агрегат имеет высокие показатели нагрузочной способности и большой ресурс наработки, что делает его эффективным с производственной и экономической точек зрения.F</p>
                        <p>При сравнительно небольшом собственном весе планетарный мотор редуктор характеризуется КПД более 90% и большим разбросом передаточных отношений. Благодаря использованию зубчатых колес (сателлитов) агрегаты отличаются высокоточной передачей крутящего момента. Мотор-редукторы с планетарной передачей находят применение в медицинском, лабораторном и другом технологичном оборудовании.F</p>
